// Variables store data in JavaScript.
// They can be declared with `var`, `let`, or `const`.
// (If you omit all three, the variable becomes a global variable.)

fullName = "Vaibhav";
console.log(fullName);

// JavaScript is dynamically typed: the type of a variable can change at runtime.
fullName = 123;
console.log(fullName);

// JavaScript is loosely typed: it can combine values of different types.
console.log(fullName + " is a number"); // The number converts to a string.

// JavaScript can also perform arithmetic if values can be converted to numbers.
console.log(fullName * 2); // The value is treated as a number here.

// JavaScript is case-sensitive, so `fullName` and `fullname` are different variables.
