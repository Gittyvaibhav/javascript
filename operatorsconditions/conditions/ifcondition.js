// An `if` statement runs code when a condition is true.
// The optional `else` block runs when the condition is false.
//
// Example:
// let color;
// if (mode === "dark-mode") {
//   color = "black";
// }

let age = 29;

if (age > 18) {
  console.log("You can vote");
} else {
  console.log("You can't vote");
}
